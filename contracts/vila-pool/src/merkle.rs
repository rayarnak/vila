/// Get the precomputed zero value for a given tree level.
pub fn get_zero_value(env: &Env, level: u32) -> U256 {
    U256::from_be_bytes(env, &soroban_sdk::Bytes::from_slice(env, &ZEROS[level as usize]))
}

use soroban_poseidon::poseidon_hash;
use soroban_sdk::crypto::bn254::Bn254Fr;

/// Hash two U256 values using Poseidon.
pub fn hash_pair(env: &Env, left: &U256, right: &U256) -> U256 {
    let mut inputs = soroban_sdk::Vec::new(env);
    inputs.push_back(left.clone());
    inputs.push_back(right.clone());

    poseidon_hash::<3, Bn254Fr>(env, &inputs)
}

/// Initialize the Merkle tree storage.
/// Uses precomputed zero values — no Poseidon calls needed.
pub fn init_tree(env: &Env) {
    for i in 0..TREE_DEPTH {
        let zero = get_zero_value(env, i);
        env.storage()
            .persistent()
            .set(&DataKey::FilledSubtree(i), &zero);
    }

    env.storage()
        .persistent()
        .set(&DataKey::NextIndex, &0u32);

    let root = get_zero_value(env, TREE_DEPTH);
    env.storage()
        .persistent()
        .set(&DataKey::CurrentRootIndex, &0u32);
    env.storage()
        .persistent()
        .set(&DataKey::Root(0), &root);
}

/// Insert a leaf into the Merkle tree. Returns the leaf index.
pub fn insert(env: &Env, leaf: &U256) -> u32 {
    let next_index: u32 = env
        .storage()
        .persistent()
        .get(&DataKey::NextIndex)
        .unwrap();

    if next_index >= TREE_CAPACITY {
        panic!("Merkle tree is full");
    }

    let mut current_index = next_index;
    let mut current_hash = leaf.clone();

    for i in 0..TREE_DEPTH {
        if current_index % 2 == 0 {
            env.storage()
                .persistent()
                .set(&DataKey::FilledSubtree(i), &current_hash);
            let zero = get_zero_value(env, i);
            current_hash = hash_pair(env, &current_hash, &zero);
        } else {
            let left: U256 = env
                .storage()
                .persistent()
                .get(&DataKey::FilledSubtree(i))
                .unwrap();
            current_hash = hash_pair(env, &left, &current_hash);
        }
        current_index /= 2;
    }

    let current_root_index: u32 = env
        .storage()
        .persistent()
        .get(&DataKey::CurrentRootIndex)
        .unwrap();
    let new_root_index = (current_root_index + 1) % ROOT_HISTORY_SIZE;

    env.storage()
        .persistent()
        .set(&DataKey::Root(new_root_index), &current_hash);
    env.storage()
        .persistent()
        .set(&DataKey::CurrentRootIndex, &new_root_index);

    let new_next = next_index + 1;
    env.storage()
        .persistent()
        .set(&DataKey::NextIndex, &new_next);

    next_index
}

/// Check if a root exists in the history ring buffer.
pub fn is_known_root(env: &Env, root: &U256) -> bool {
    let current_root_index: u32 = env
        .storage()
        .persistent()
        .get(&DataKey::CurrentRootIndex)
        .unwrap_or(0);

    for i in 0..ROOT_HISTORY_SIZE {
        let idx = (current_root_index + ROOT_HISTORY_SIZE - i) % ROOT_HISTORY_SIZE;
        if let Some(stored_root) = env
            .storage()
            .persistent()
            .get::<DataKey, U256>(&DataKey::Root(idx))
        {
            if stored_root == *root {
                return true;
            }
        } else {
            break;
        }
    }
    false
}

/// Get the latest root.
pub fn get_last_root(env: &Env) -> U256 {
    let current_root_index: u32 = env
        .storage()
        .persistent()
        .get(&DataKey::CurrentRootIndex)
        .unwrap();
    env.storage()
        .persistent()
        .get(&DataKey::Root(current_root_index))
        .unwrap()
}
